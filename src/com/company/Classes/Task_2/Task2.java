package com.company.Classes.Task_2;

public class Task2 {
    private int x;
    private int y;

    public Task2(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Task2() {
        this.x = 0;
        this.y = 1;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
